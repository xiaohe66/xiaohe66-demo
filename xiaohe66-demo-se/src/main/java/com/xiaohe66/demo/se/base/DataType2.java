public class DataType2 {

    public static void main(String[] args) {

        // 2进制，0b开头
        System.out.println(0b101);

        // 8进制，0开头
        System.out.println(011);

        // 10进制，默认
        System.out.println(11);

        // 16进制，0x开头
        System.out.println(0xAA);

    }
}
